export function getDictionary(domain) {
  if (domain === 'auto' || !domain) {
    return { ...COMMON, ...PHRASES };
  }
  const domainDict = ALL_DOMAINS[domain];
  return domainDict ? { ...COMMON, ...PHRASES, ...domainDict } : { ...COMMON, ...PHRASES };
}
